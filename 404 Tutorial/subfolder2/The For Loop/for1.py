# Variable Initiation
i = 0
total = 0
# range(start, stop, step) (Initialize control, Check condition, Update control)
# i = index (shortand)
for i in range (1, 4, 1):
    # Execute body
    total = total + i
    # /t tab space (for cleaner formatting)
    print("i = ", i, "\t total", total)
print("Done")

